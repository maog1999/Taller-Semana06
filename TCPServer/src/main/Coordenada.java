package main;

public class Coordenada {

	//Atributos
	private int x,y;
	private String a;
	
	//Constructores
	public Coordenada(String a) {
		this.a = a;
	}

	
	//Metodos
	


	public int getX() {
		return x;
	}


	public String getA() {
		return a;
	}


	public void setA(String a) {
		this.a = a;
	}


	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}


	
	
	
	
	
	
}
